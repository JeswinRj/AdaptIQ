"""Lesson composer: retrieved chunks -> ONE structured lesson, not a list.

With AI: synthesis grounded in retrieved chunks, with source citations.
Without AI: honest structured assembly grouped by resource type.
When the library is thin and AI is available: generate CBSE-aligned notes,
INGEST them (Tier 1, 'ai-generated'), then compose — the library densifies
itself with use (docs/content_architecture.md §4).
"""
import config
from src.library import pipeline, search

SECTION_ORDER = [("notes", "Core ideas"), ("textbook", "From the textbook"),
                 ("formula", "Formulas"), ("example", "Worked examples"),
                 ("practice", "Practice"), ("summary", "Quick revision"),
                 ("reference", "Reference")]

MATH_STYLE = (" Write every formula and mathematical expression in LaTeX: "
              "inline as \\( ... \\), standalone equations as \\[ ... \\]. "
              "Never spell maths out in words when notation exists.")


def _ask(prompt: str):
    from src.dashboard.app import _ask_ai
    return _ask_ai(prompt)


def _generate_notes_if_thin(con, query: str, profile: dict, hits: list) -> list:
    body_hits = [h for h in hits if not h["link_only"]]
    if len(body_hits) >= 3 or not config.AI_PROVIDER:
        return hits
    result = _ask(
        f"Write structured study notes on '{query}' for CBSE Class 11/12 "
        "Mathematics. Sections, each starting with a '## ' heading: Overview, "
        "Key Concepts, Definitions, Important Formulas, Worked Examples (two, "
        "fully solved), Common Mistakes, Practice Questions (three), Quick "
        "Revision Summary. Plain text, no markdown besides the '## ' "
        f"headings.{MATH_STYLE}")
    if not result["available"]:
        return hits
    pipeline.ingest(con, title=f"Study notes: {query}", text=result["text"],
                    source="ai-generated", license="platform",
                    redistribute_allowed=True, resource_type="notes",
                    attribution="Generated by Adapt IQ")
    return search.hybrid_search(con, query, k=12, profile=profile)


def answer_from_doc(con, doc: dict, question: str,
                    audience: str = "standard") -> dict:
    """'Answer from this PDF only': grounded strictly in one document's
    chunks. With AI: a cited answer that admits when the document doesn't
    cover the question. Without AI: the best-matching excerpts, honestly
    labelled. Never mixes in other library content."""
    hits = search.doc_search(con, doc["id"], question, k=6)
    body = [h for h in hits if not h["link_only"] and h["score"] > 0.25]
    if not body:
        return {"mode": "empty", "text": "", "excerpts": [],
                "message": "No part of this document matches that question."}

    if config.AI_PROVIDER:
        numbered = "\n\n".join(f"[{i+1}] {h['text'][:900]}"
                               for i, h in enumerate(body[:5]))
        style = (" Use very simple language: short sentences, one idea each."
                 if audience == "simplified" else "")
        result = _ask(
            f"Answer the student's question USING ONLY these excerpts from "
            f"the document '{doc['title']}'. Cite excerpts inline like [1]. "
            "If the excerpts do not contain the answer, say exactly: "
            "\"This document doesn't cover that.\" and stop — do not add "
            f"outside knowledge.{style}{MATH_STYLE}\n\n"
            f"QUESTION: {question}\n\nEXCERPTS:\n{numbered}")
        if result["available"]:
            return {"mode": "ai", "text": result["text"],
                    "excerpts": body[:5], "message": ""}

    return {"mode": "excerpts", "text": "", "excerpts": body[:3],
            "message": "AI is off, so here are the document sections that "
                       "best match your question."}


def build_lesson(con, query: str, profile: dict = None,
                 audience: str = "standard", visibility_for: str = "") -> dict:
    hits = search.hybrid_search(con, query, k=12, profile=profile,
                                visibility_for=visibility_for)
    hits = _generate_notes_if_thin(con, query, profile, hits)
    body = [h for h in hits if not h["link_only"]]
    links = []
    seen_docs = set()
    for h in hits:
        if h["link_only"] and h["doc_id"] not in seen_docs and h["url"]:
            links.append(h)
            seen_docs.add(h["doc_id"])

    sources = []
    seen = set()
    for h in body:
        if h["doc_id"] not in seen:
            sources.append({"n": len(sources) + 1, "title": h["title"],
                            "source": h["source"], "license": h["license"],
                            "attribution": h["attribution"], "url": h["url"]})
            seen.add(h["doc_id"])
    src_no = {s["title"]: s["n"] for s in sources}

    # AI synthesis, grounded in the retrieved chunks only
    if config.AI_PROVIDER and body:
        numbered = "\n\n".join(
            f"[{src_no[h['title']]}] ({h['resource_type']}) {h['text'][:900]}"
            for h in body[:8])
        style = (" Use very simple language: short sentences, one idea each, "
                 "no metaphors." if audience == "simplified" else "")
        result = _ask(
            f"Compose one structured lesson on '{query}' for a CBSE Class "
            "11/12 maths student USING ONLY the numbered source excerpts "
            "below — do not add outside facts. Cite excerpts inline like [1]. "
            "Sections, each starting '## ': Overview, Key Ideas, Worked "
            f"Example, Practice, Quick Revision.{style}{MATH_STYLE}"
            f"\n\nSOURCES:\n{numbered}")
        if result["available"]:
            return {"mode": "ai", "query": query, "text": result["text"],
                    "sections": [], "sources": sources, "links": links,
                    "hits": len(body)}

    # offline: structured assembly grouped by resource type
    sections = []
    for rtype, label in SECTION_ORDER:
        items = [h for h in body if h["resource_type"] == rtype][:3]
        if items:
            sections.append({"label": label, "items": items})
    if not sections and body:
        sections = [{"label": "From the library", "items": body[:5]}]
    return {"mode": "assembled", "query": query, "text": "",
            "sections": sections, "sources": sources, "links": links,
            "hits": len(body)}
